/*---------------------------------------------------------------------------
  UTF-16の文字列の長さを得る
    *utf16: 文字列(UTF-16)へのポインタ
    return: 文字数
---------------------------------------------------------------------------*/
u16 libConvUtf16len(const u16 *utf16)
{
  u16 len = 0;
  while(utf16[len] != '\0')
    len++;
  return len;
}

