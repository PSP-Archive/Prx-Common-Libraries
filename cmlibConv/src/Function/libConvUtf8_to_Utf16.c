
#include "common.h"

/*---------------------------------------------------------------------------
  UFT-8Nの文字列をUTF-16に変換する
    *utf16: 変換後の文字列(UTF-16)へのポインタ
    *utf8: UFT-8N文字列へのポインタ
---------------------------------------------------------------------------*/
void libConvUtf8_to_Utf16(u16 *utf16, const char *utf8)
{
  while(*utf8 !='\0')
  {
    utf8 = libConvUtf8_Utf16(utf16++, utf8);
  }
  *utf16 = '\0';
}
